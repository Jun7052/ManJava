package java20180518;

public class Person {
	String name;
	int soju;
	
	public Person(String name, int soju) {
		super();
		this.name = name;
		this.soju = soju;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getSoju() {
		return soju;
	}

	public void setSoju(int soju) {
		this.soju = soju;
	}
	
}
